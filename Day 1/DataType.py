print("----- BASIC DATA TYPES IN PYTHON -----")

# 1. Integer (int)
age = 20
print("Age:", age)
print("Data Type:", type(age))

# 2. Float (float)
height = 5.8
print("\nHeight:", height)
print("Data Type:", type(height))

# 3. String (str)
name = "Suraj"
print("\nName:", name)
print("Data Type:", type(name))

# 4. Boolean (bool)
is_student = True
print("\nIs Student:", is_student)
print("Data Type:", type(is_student))

print("\n----- END -----")