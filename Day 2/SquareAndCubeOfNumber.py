# Program: Calculate Square and Cube of a Number
# Concept: Taking Integer Input, Exponentiation Operator (**), and Output Formatting

# Step 1: Input a number from the user and convert to integer
num = int(input("Enter a number: "))

# Step 2: Calculate Square (num ** 2) and Cube (num ** 3)
square = num ** 2
cube = num ** 3

# Step 3: Display the calculated square and cube results
print("Square =", square)
print("Cube =", cube)