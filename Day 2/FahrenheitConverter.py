# Program: Temperature Converter (Celsius to Fahrenheit)
# Concept: Float Data Type, Arithmetic Formula, and Operator Precedence

# Step 1: Input temperature in Celsius from user
celsius = float(input("Enter temperature in Celsius: "))

# Step 2: Convert Celsius to Fahrenheit using formula: F = (C * 9/5) + 32
fahrenheit = (celsius * 9/5) + 32

# Step 3: Display converted temperature
print("Temperature in Fahrenheit =", fahrenheit)