# Program: Add Two Numbers
# Concept: Taking Integer Input, Type Casting (int), and Addition Operator (+)

# Step 1: Accept integer inputs from user
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

# Step 2: Perform addition
total_sum = num1 + num2

# Step 3: Display the sum
print("Sum =", total_sum)