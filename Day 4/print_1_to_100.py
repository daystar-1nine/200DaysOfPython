# Program: Print Numbers from 1 to 100
# Concept: Simple loop iterations (for and while loops)

print("--- Printing 1 to 100 using while loop ---")
count = 1
while count <= 100:
    print(count, end=" ")
    count += 1
print()

print("\n--- Printing 1 to 100 using for loop ---")
for i in range(1, 101):
    print(i, end=" ")
print()
